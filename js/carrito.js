

document.querySelector(".dropdown-container").addEventListener("click",function(){
    document.querySelector(".dropdown-container ul").classList.toggle("show");
})

function agregarAlCarrito(nombre,precio,cantidad){
     
      $("#carrito").append(`<tr> <td>  `+nombre + ` </td>  <td> ` +precio+ `</td> <td>`+cantidad+`</td> </tr>`); 

}
$("#1").click(()=> {
    let nombre= "Aventura F";
    let precio= "$3295"
    let cantidad= "1";
    console.log("nombre:"+ nombre )
    console.log("precio :"+ precio)
    console.log("cantidad :"+ cantidad)
  agregarAlCarrito(nombre, precio,cantidad);
})

